#!/usr/bin/env bash
# ── h-config.sh ───────────────────────────────────────────────────────────────
# Called by HiveOS to generate the miner configuration file from flight-sheet
# parameters before h-run.sh starts the miner.
#
# HiveOS injects these environment variables from the flight-sheet:
#
#   CUSTOM_TEMPLATE     — Mining reward address (wallet)
#   CUSTOM_URL          — Xenom node gRPC endpoint  e.g. "192.168.1.10:36669"
#   CUSTOM_PASS         — (unused by genome-miner)
#   CUSTOM_USER_CONFIG  — Extra CLI flags, e.g. "--gpu all --mainnet"
#   CUSTOM_CONFIG_FILENAME — Full path where config file must be written
#   WORKER_NAME         — HiveOS rig worker name (informational)
#
# Output: a shell-sourceable config file at $CUSTOM_CONFIG_FILENAME containing
# the complete argument string that h-run.sh will pass to genome-miner.
# ─────────────────────────────────────────────────────────────────────────────

set -euo pipefail

MINER_NAME="genome-miner"
MINER_DIR="/hive/miners/${MINER_NAME}"
CONFIG_FILE="${CUSTOM_CONFIG_FILENAME:-${MINER_DIR}/${MINER_NAME}.conf}"

# ── Validate required fields ──────────────────────────────────────────────────
if [[ -z "${CUSTOM_TEMPLATE:-}" ]]; then
    echo "[h-config] ERROR: Mining address not set (CUSTOM_TEMPLATE is empty)."
    echo "           Set the Wallet field in the HiveOS flight-sheet."
    exit 1
fi

MINING_ADDRESS="${CUSTOM_TEMPLATE}"

# ── Parse node endpoint ───────────────────────────────────────────────────────
# Accept raw "host:port" or URLs like "grpc://host:port", "stratum+tcp://host:port"
RPCSERVER="${CUSTOM_URL:-localhost:36669}"
RPCSERVER="${RPCSERVER#*://}"           # strip any scheme prefix
RPCSERVER="${RPCSERVER%%/*}"            # strip any trailing path
RPCSERVER="${RPCSERVER:-localhost:36669}"

# ── GPU selection ─────────────────────────────────────────────────────────────
# Default to all GPUs; user can override via CUSTOM_USER_CONFIG (e.g. --gpu 0,1)
GPU_ARG="--gpu all"
if echo "${CUSTOM_USER_CONFIG:-}" | grep -qE '\-\-gpu[[:space:]]'; then
    GPU_ARG=""   # user specified --gpu explicitly in extra config
fi

# ── Network flag ──────────────────────────────────────────────────────────────
# Default to mainnet; user can add --testnet or --devnet via CUSTOM_USER_CONFIG
NETWORK_ARG="--mainnet"
if echo "${CUSTOM_USER_CONFIG:-}" | grep -qE '\-\-(testnet|devnet)'; then
    NETWORK_ARG=""   # user specified network explicitly
fi

# ── Assemble argument string ──────────────────────────────────────────────────
MINER_ARGS="--mining-address ${MINING_ADDRESS}"
MINER_ARGS="${MINER_ARGS} --rpcserver ${RPCSERVER}"
MINER_ARGS="${MINER_ARGS} ${NETWORK_ARG}"
MINER_ARGS="${MINER_ARGS} ${GPU_ARG}"
MINER_ARGS="${MINER_ARGS} --no-tui"
MINER_ARGS="${MINER_ARGS} --api-port 4000"

# Append any extra user-supplied flags (preserve order)
if [[ -n "${CUSTOM_USER_CONFIG:-}" ]]; then
    MINER_ARGS="${MINER_ARGS} ${CUSTOM_USER_CONFIG}"
fi

# Trim excess whitespace
MINER_ARGS="$(echo "${MINER_ARGS}" | tr -s ' ' | sed 's/^ //;s/ $//')"

# ── Write config file ─────────────────────────────────────────────────────────
mkdir -p "$(dirname "${CONFIG_FILE}")"

cat > "${CONFIG_FILE}" << EOF
# genome-miner HiveOS config — generated by h-config.sh
# Worker: ${WORKER_NAME:-unknown}  Node: ${RPCSERVER}
MINER_ARGS="${MINER_ARGS}"
EOF

echo "[h-config] Config written to: ${CONFIG_FILE}"
echo "[h-config] Args: ${MINER_ARGS}"
